package id.co.map.spk.utils;

public class AppProperty {

    private String email;
    private String baseUrl;
    private String compHeaderImagePath;
    private String spkQuotationPath;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getBaseUrl() {
        return baseUrl;
    }

    public void setBaseUrl(String baseUrl) {
        this.baseUrl = baseUrl;
    }

    public String getCompHeaderImagePath() {
        return compHeaderImagePath;
    }

    public void setCompHeaderImagePath(String compHeaderImagePath) {
        this.compHeaderImagePath = compHeaderImagePath;
    }

    public String getSpkQuotationPath() {
        return spkQuotationPath;
    }

    public void setSpkQuotationPath(String spkQuotationPath) {
        this.spkQuotationPath = spkQuotationPath;
    }
}
